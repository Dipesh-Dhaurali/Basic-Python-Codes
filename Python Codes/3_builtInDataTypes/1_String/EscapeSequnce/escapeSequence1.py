# 1 ) Single Quote (') => (\')
print('It\'s raining')

# 2 ) Double Quote (") => (\")
print("\"Sita\" and \"Gita\" are best friend ")

# 3) BackSlash (\) => (\\)
print('He\\She want to go USA')

# 4) Newline (\n)
print("My friend name is rekha \n and my brother name is nimesh")

#5) Tab Space (\t)
print("Hello I am from Dang, \t I live in Kathmandu")

#6) BackSpace (remove one space)
print("Hello I am from Dang \bI live in Kathmandu")




