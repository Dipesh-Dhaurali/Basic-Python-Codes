# convert tuple to string
tup1=("apple","banana","carrot")

str1=str(tup1) #use str constructor to convert tuple to string
print(str1) #('apple', 'banana', 'carrot')
print(type(str1)) #<class 'str'>



# convert string to tuple
str2="Hello"

tup2=tuple(str2)
print(tup2) #('H', 'e', 'l', 'l', 'o')
print(type(tup2)) #<class 'tuple'>


