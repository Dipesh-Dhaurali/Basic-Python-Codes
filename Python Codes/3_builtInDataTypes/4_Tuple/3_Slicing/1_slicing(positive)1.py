tup1=(11,22,33,41,52)

#note always use big braces for slicing
#positve slicing
print(tup1[:])    #(11, 22, 33, 41, 52)
print(tup1[0:])   #(11, 22, 33, 41, 52)
print(tup1[:5])   #(11, 22, 33, 41, 52)
print(tup1[0:5])  #(11, 22, 33, 41, 52)

print(tup1[1:3])  #(22, 33)
print(tup1[3:5])  #(41, 52)
print(tup1[3:4])  #(41,)
print(tup1[0:4])   #(11, 22, 33, 41)
print(tup1[1:4])   #(22, 33, 41)


