#for looping indexing in tuple # positive indexing

#tuple in numeric data
tup1=(11,21,31,41,51)
for i in tup1:
    print(i,end=" ") # 11 21 31 41 51

print()

#tuple in string data
fruits=("apple","banana","grapes","orange","mango")
for i in fruits:
    print(i,end=" ") #apple banana grapes orange mango