# making list with list(Constructor)

a=list(('red','blue','yellow','black'))
print(a)     #['red', 'blue', 'yellow', 'black']

b=list("red")
print(b)     #['r', 'e', 'd']

c=list(["red"])
print(c)     #['red']

d=list()
print(d)    #[]

e=list((1,2,3,4))
print(e)    #[1, 2, 3, 4]

f=list("1234")
print(f)    #['1', '2', '3', '4']

g=list(["1234"])
print(g)     #['1234']



